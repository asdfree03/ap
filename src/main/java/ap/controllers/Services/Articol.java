package ap.controllers.Services;

public class Articol {
    public int id;
    public String nume;
    public String poza;
    public String descriere;

    public Articol(int id, String nume, String poza, String descriere) {
        this.id = id;
        this.nume = nume;
        this.poza = poza;
        this.descriere = descriere;
    }

}
